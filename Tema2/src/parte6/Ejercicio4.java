package parte6;

public class Ejercicio4 {

	public static void main(String[] args) {
		



				// Introducimos la variable suma donde se almacena la suma

				int suma = 0;

			

				// Introducimos el bucle for, donde el contador será las veces que se introduce

				// un número dentro del bucle hasta llegar a 10, y la i serán los números impares

				// que se van sumando

				for (int i = 1, cont = 1; cont <= 10; i += 2, cont++) {



					// Vamos sumando números

					suma = suma + i;



				}

				// Imprimimos la suma

				System.out.println(suma);

				


	}

}
